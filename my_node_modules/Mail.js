'use strict';

var validator      = require('validator');

var mandrill       = require('mandrill-api/mandrill');
var MandrillClient = require(global.rootDir + '/config/mandrill.js')(mandrill);


module.exports.sendFeedback = function(req, res) {

	// Get inputs (SHOULD CLEAN THEM)
	var name = req.body.name;
	var email = req.body.email;
	var subject = req.body.subject;
	var message = req.body.message;

	// Validate inputs
	if (email.length !== 0) {
		if (validator.isEmail(email) === false) {
			return res.status(400).json({ status: 400, error: 'Invalid email.' });
		}
	}
	else {
		// If the email wasn't set by the user, use a default one
		email = 'prioritize.user@prioritize.com';
	}

	if (message.length === 0) {
		return res.status(400).json({ status: 400, error: 'Feedback cannot be empty.' });
	}

	var data = {
		'name': name,
		'email': email,
		'subject': subject,
		'message': message
	};

	// Actually send the email
	sendEmailToAdmin(data,
		function(err, result) {
			if (err) {
				return res.status(500).json({ status: 500, error: 'Your email could not be sent. Please try again.' });
			}

			return res.status(200).json({ status: 200 });
		}
	);
};

function sendEmailToAdmin(data, callback) {	
	var message = {
	    'text': data.message,
	    'subject': data.subject,
	    'from_email': data.email,
	    'from_name': data.name,
	    'to': [{
	            'email': 'prioritize.app@gmail.com',
	            'name': 'PrioritizeAdmin',
	            'type': 'to'
	    }]
	};

    MandrillClient.messages.send({ 'message': message },
    	function (result) {
    		callback(null, result);
    	},
    	function (e) {
    		callback(e);
    	}
    );
}
